// Loops

// Numbers up to ten:

for (let i = 0; i <= 10; i++) {
  console.log(i);
}

let i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

// Even numbers to 20:

for (let i = 0; i <= 20; i += 2) {
  console.log(i);
}

// Back to zero:

for (let i = 10; i >= 0; i--) {
  console.log(i);
}
